/*FIND THE GREATER VALUE IN TWO NUMBER USING IF*/
#include<iostream>
using namespace std;
int main()
{
    /*variable declaration*/
    int a,b;
    cout<<"Enter a:\n";
    cin>>a;
    cout<<"Enter b:\n";
    cin>>b;
    if(a>b)
        cout<<a<<"is greater";
    if(b>a)
        cout<<b<<" is greater";
}
